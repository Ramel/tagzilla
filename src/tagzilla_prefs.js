// *** TagZilla default preferences ***

// Default tagline file
pref("tagzilla.default.file", "");

// Save last-used file as new default?
pref("tagzilla.default.autosave", true);

// Save tagline files as multiline by default?
pref("tagzilla.multiline.file", false);

// Default character set of tagline files
pref("tagzilla.file.charset", "UTF-8");

// Use this string, on a line by itself, to delimit multi-line taglines
pref("tagzilla.multiline.delimiter", "%");

// Prefix and suffix added to taglines placed in the clipboard
pref("tagzilla.clipboard.prefix", "");
pref("tagzilla.clipboard.suffix", "");

// Prefix and suffix added to taglines inserted into email
pref("tagzilla.mail.prefix", "\\n-=-=-\\n... ");
pref("tagzilla.mail.suffix", "\\n* TagZilla 0.056 * http://tagzilla.mozdev.org");

// Convert '\n' to a newline character in taglines?
pref("tagzilla.newline.convert", true);

// Run TagZilla when hotkey pressed in a textarea?
pref("tagzilla.textbox.trapkey", true);

// Pick a tagline for the user when they're in a textarea
// (without opening window)?
pref("tagzilla.textbox.pick", false);

